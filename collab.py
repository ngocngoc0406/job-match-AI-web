import math

def retrieval_difficulty(scores, k=10):
    ranked = sorted(scores.values(), reverse=True)
    topk = ranked[:k]

    mean_score = sum(topk) / k
    gap = topk[0] - topk[-1]

    probs = [s / (sum(topk) + 1e-9) for s in topk]
    entropy = -sum(p * math.log(p + 1e-9) for p in probs)

    return mean_score, gap, entropy

