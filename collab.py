def ndcg_at_k_single_gt(ranked, gt, k=10):
    """NDCG@K for single ground-truth item with binary relevance."""
    if gt not in ranked[:k]:
        return 0.0
    r = ranked.index(gt) + 1
    return 1.0 / log2(r + 1)

def make_pseudo_cv_from_job(job_node, keep_ratio=0.35, drop_core_ratio=0.4, remove_text_ratio=0.92):
    """
    Generate pseudo-CV from a job (HARDENED VERSION):
    - Drop a portion of skills (including some core skills) to avoid 100% match
    - Lower skill probabilities with variance to simulate real CV uncertainty
    - Minimal text overlap to prevent leakage

    Args:
        job_node: source job node
        keep_ratio: fraction of skills to keep (default 0.35 = keep only 35%)
        drop_core_ratio: fraction of core skills to drop (default 0.4 = drop 40% core skills)
        remove_text_ratio: fraction of text to remove (default 0.92 = keep only 8%)
    """
    all_skills = list(job_info[job_node]["prob_skills"].keys())

    core_skills = [s for s in all_skills if s in CORE_SKILLS_CANON]
    other_skills = [s for s in all_skills if s not in CORE_SKILLS_CANON]

    random.shuffle(core_skills)
    keep_core_n = max(0, int(len(core_skills) * (1 - drop_core_ratio)))
    kept_core = core_skills[:keep_core_n]

    random.shuffle(other_skills)
    keep_other_n = max(1, int(len(other_skills) * keep_ratio))
    kept_other = other_skills[:keep_other_n]

    keep = kept_core + kept_other

    pseudo_skills = {}
    for s in keep:
        if s in CORE_SKILLS_CANON:
            pseudo_skills[s] = random.uniform(0.45, 0.70)
        else:
            pseudo_skills[s] = random.uniform(0.30, 0.55)

