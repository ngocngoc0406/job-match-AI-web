def compute_skill_coverage(user_skills, job_skills):
    """
    Compute skill coverage score only (not full user_job_score).
    This is the correct metric for fidelity testing.
    """
    if not job_skills:
        return 0.0

    num = 0.0
    den = 0.0

    for sk, pj in job_skills.items():
        w = 2.0 if sk in CORE_SKILLS_CANON else 1.0
        pu = float(user_skills.get(sk, 0.0))
        num += min(pu, pj) * w
        den += pj * w

    return num / den if den > 0 else 0.0


def fidelity_test_skill_coverage(user_skills, job_skills, skill_to_remove):
    """
    Fidelity test on SKILL COVERAGE SCORE (paper-safe).

    Remove one skill from user profile and measure skill_coverage drop.
    This isolates the effect of the skill on the matching score.
    """

    original_coverage = compute_skill_coverage(user_skills, job_skills)

    ablated_skills = dict(user_skills)
    if skill_to_remove in ablated_skills:
        del ablated_skills[skill_to_remove]

    ablated_coverage = compute_skill_coverage(ablated_skills, job_skills)

    drop = original_coverage - ablated_coverage
    return original_coverage, ablated_coverage, drop


