import hashlib

def sid(prefix: str, raw: str, n=8):
    raw = "" if raw is None else str(raw)
    base = norm_text(raw)
    h = hashlib.md5(base.encode("utf-8")).hexdigest()[:n]
    slug = re.sub(r"[^a-z0-9]+", "_", base).strip("_")
    return f"{prefix}_{slug}_{h}"

