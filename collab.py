# Skill scoring variants for ablation

def skill_score_binary(user_skills, job_skills, tau_u=0.6, tau_j=0.6):
    """
    Strict binary overlap
    """
    if not user_skills or not job_skills:
        return 0.0

    U = {s for s, p in user_skills.items() if p >= tau_u}
    J = {s for s, p in job_skills.items() if p >= tau_j}

    if not U or not J:
        return 0.0

    return len(U & J) / len(J)


def skill_score_deterministic(user_skills, job_skills):
    """
    Deterministic prob matching:
    sum_c min(p_u(c), p_j(c)) / |C_j|
    """
    if not user_skills or not job_skills:
        return 0.0

    score = 0.0
    for c, pj in job_skills.items():
        pu = user_skills.get(c, 0.0)
        score += min(pu, pj)

    return score / max(1, len(job_skills))


def skill_score_expectation(user_skills, job_skills):
    """
    Expectation-based coverage (your main contribution)
    """
    if not user_skills or not job_skills:
        return 0.0

    num = 0.0
    den = 0.0

    for c, pj in job_skills.items():
        pu = user_skills.get(c, 0.0)
        num += min(pu, pj)
        den += pj

    return num / max(den, 1e-9)

