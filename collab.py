def prob_skill_sim_job_job(Aprob: dict, Bprob: dict):
    if not Aprob or not Bprob:
        return 0.0

    keys = set(Aprob.keys()) | set(Bprob.keys())
    num, den = 0.0, 0.0

    for s in keys:
        w = 2.0 if s in CORE_SKILLS_CANON else 1.0
        a = float(Aprob.get(s, 0.0))
        b = float(Bprob.get(s, 0.0))
        num += min(a, b) * w
