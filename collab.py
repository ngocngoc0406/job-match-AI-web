def norm_text(t):
    if t is None:
        return ""
    s = unicodedata.normalize("NFKD", str(t).lower())
    s = "".join(c for c in s if not unicodedata.combining(c))
    s = re.sub(r"\s+", " ", s).strip()
    return s

def safe_id(t):
    s = norm_text(t)
    s = re.sub(r"[^a-z0-9]+", "_", s).strip("_")
    return s if s else "unknown"

